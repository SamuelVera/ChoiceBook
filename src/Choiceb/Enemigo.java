package ChoiceB;

public class Enemigo{
    private int HP;
    private int ATK;
    private int DEF;
    private int SUE;

    public int getHP() {
        return HP;
    }

    public void setHP(int HP) {
        this.HP = HP;
    }

    public int getATK() {
        return ATK;
    }

    public void setATK(int ATK) {
        this.ATK = ATK;
    }

    public int getDEF() {
        return DEF;
    }

    public void setDEF(int DEF) {
        this.DEF = DEF;
    }

    public int getSUE() {
        return SUE;
    }

    public void setSUE(int SUE) {
        this.SUE = SUE;
    }
    
}
