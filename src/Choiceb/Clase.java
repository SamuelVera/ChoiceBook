package Choiceb;

public class Clase {
    
    private int HP;
    private int ATK;
    private int DEF;
    private int SAN;
    private int SUE;
    private char sexo;
    private String NAME;

    public int getHP() {
        return HP;
    }

    public void setHP(int HP) {
        this.HP = HP;
    }

    public int getATK() {
        return ATK;
    }

    public void setATK(int ATK) {
        this.ATK = ATK;
    }

    public int getDEF() {
        return DEF;
    }

    public void setDEF(int DEF) {
        this.DEF = DEF;
    }

    public int getSAN() {
        return SAN;
    }

    public void setSAN(int SAN) {
        this.SAN = SAN;
    }

    public int getSUE() {
        return SUE;
    }

    public void setSUE(int SUE) {
        this.SUE = SUE;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }
    
    
}
